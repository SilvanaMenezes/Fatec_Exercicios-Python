#9. Faça um algoritmo que calcule e apresente o valor do volume de uma lata de óleo, dado seu raio e sua altura.


from cmath import pi
print('--'*5, 'Calculo de Volume', '--'*5)
altura = float(input('digite o valor para a altura : '))
raio =  float(input('digite o valor para o raio : '))
volume = pi * raio ** 2 * altura 

print('O valor da aoltura é: ', volume)






    
