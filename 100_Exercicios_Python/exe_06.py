# 6. Ler dois números inteiros e exibir o quociente e o resto da divisão inteira entre eles;

n1 = (int(input('Digite o n1: ')))
print ('O numero digitado foi: ',n1)

n2 = (int(input('Digite o n2: ')))
print ('O numero digitado foi: ',n2)

divisao = n1 // n2
print ('O quociente da divisão entre os dois números é: ', divisao)

resto = n1 % n2
print ('O resto da divisão dos dois número é: ', resto)

  